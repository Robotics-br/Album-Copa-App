function buildStickers(): Sticker[] {
  const result: Sticker[] = [];
  let globalIndex = 1;

  for (const team of teams) {
    let localIndex = 1;

    result.push({
      code: `${team.code}${localIndex}`,
      albumIndex: globalIndex++,
      name: 'badge',
      section: team.id,
      isShiny: true,
    });
    localIndex++;

    result.push({
      code: `${team.code}${localIndex}`,
      albumIndex: globalIndex++,
      name: 'team',
      section: team.id,
      isShiny: false,
    });
    localIndex++;

    const squad = squads[team.id] ?? [];
    for (const playerName of squad) {
      result.push({
        code: `${team.code}${localIndex}`,
        albumIndex: globalIndex++,
        name: playerName,
        section: team.id,
        isShiny: false,
      });
      localIndex++;
    }
  }

  return result;
}

export const stickers: Sticker[] = buildStickers();
export const totalStickers = stickers.length;

const stickersBySection = new Map<string, Sticker[]>();
for (const s of stickers) {
  const list = stickersBySection.get(s.section);
  if (list) list.push(s);
  else stickersBySection.set(s.section, [s]);
}

export const teamMap = new Map(teams.map((t) => [t.id, t]));

export function getStickersByTeam(sectionId: string): Sticker[] {
  return stickersBySection.get(sectionId) ?? [];
}

export function getTeamById(teamId: string): Team | undefined {
  return teams.find((t) => t.id === teamId);
}

export function getStickerByCode(code: string): Sticker | undefined {
  return stickers.find((s) => s.code === code);
}
